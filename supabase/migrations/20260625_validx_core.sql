drop policy if exists "Profiles are visible to self, related businesses, and admins" on public.profiles;
create policy "Profiles are visible to self, related businesses, and admins"
on public.profiles for select
to authenticated
using (
  id = auth.uid()
  or public.is_admin()
  or exists (
    select 1
    from public.submissions s
    join public.experiments e on e.id = s.experiment_id
    where s.tester_id = profiles.id
      and e.owner_id = auth.uid()
  )
);

drop policy if exists "Users can create their public profile" on public.profiles;
create policy "Users can create their public profile"
on public.profiles for insert
to authenticated
with check (
  id = auth.uid()
  and role in ('business', 'tester')
  and status = 'active'
);

