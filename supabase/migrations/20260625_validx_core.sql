create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  role text not null check (role in ('business', 'tester', 'admin')),
  email text not null,
  auth_method text not null default 'supabase',
  first_name text not null default 'New',
  last_name text not null default 'User',
  company text,
  industry text,
  company_size text,
  school text,
  major text,
  age integer,
  status text not null default 'active' check (status in ('active', 'suspended', 'banned')),
  created_at timestamptz not null default now()
);

create unique index if not exists profiles_email_idx on public.profiles (lower(email));
create index if not exists profiles_role_idx on public.profiles (role);

create table if not exists public.experiments (
  id bigint generated by default as identity primary key,
  owner_id uuid not null references public.profiles(id) on delete cascade,
  title text not null,
  assumption text not null,
  type text not null,
  tier text not null check (tier in ('quick-test', 'deep-dive', 'full-study')),
  budget integer not null check (budget > 0),
  reach integer not null check (reach > 0),
  status text not null default 'active' check (status in ('draft', 'active', 'completed', 'archived')),
  paid_with text,
  payment_ref text,
  created_at timestamptz not null default now()
);

create index if not exists experiments_owner_idx on public.experiments (owner_id);
create index if not exists experiments_status_created_idx on public.experiments (status, created_at desc);

create table if not exists public.experiment_claims (
  id bigint generated by default as identity primary key,
  experiment_id bigint not null references public.experiments(id) on delete cascade,
  tester_id uuid not null references public.profiles(id) on delete cascade,
  status text not null default 'claimed' check (status in ('claimed', 'submitted', 'cancelled')),
  created_at timestamptz not null default now(),
  unique (experiment_id, tester_id)
);

create index if not exists experiment_claims_tester_idx on public.experiment_claims (tester_id, status);

create table if not exists public.submissions (
  id bigint generated by default as identity primary key,
  experiment_id bigint not null references public.experiments(id) on delete cascade,
  tester_id uuid not null references public.profiles(id) on delete cascade,
  notes text not null,
  files_json jsonb not null default '[]'::jsonb,
  payout integer not null default 0,
  status text not null default 'submitted' check (status in ('submitted', 'approved', 'rejected')),
  admin_note text,
  created_at timestamptz not null default now(),
  unique (experiment_id, tester_id)
);

create index if not exists submissions_experiment_idx on public.submissions (experiment_id);
create index if not exists submissions_tester_idx on public.submissions (tester_id, status);

create table if not exists public.payouts (
  id bigint generated by default as identity primary key,
  tester_id uuid not null references public.profiles(id) on delete cascade,
  amount integer not null check (amount > 0),
  method text not null check (method in ('stripe', 'paypal')),
  status text not null default 'pending' check (status in ('pending', 'processing', 'paid', 'failed')),
  reference text,
  created_at timestamptz not null default now(),
  paid_at timestamptz
);

create index if not exists payouts_tester_idx on public.payouts (tester_id, status);

create table if not exists public.audit_log (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete set null,
  action text not null,
  details jsonb,
  created_at timestamptz not null default now()
);

create or replace function public.current_profile_role()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select role from public.profiles where id = auth.uid() limit 1
$$;

create or replace function public.current_profile_status()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select status from public.profiles where id = auth.uid() limit 1
$$;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(public.current_profile_role() = 'admin', false)
$$;

create or replace function public.available_balance_for(tester uuid)
returns integer
language sql
stable
security definer
set search_path = public
as $$
  with earned as (
    select coalesce(sum(payout), 0)::integer as total
    from public.submissions
    where tester_id = tester
      and status in ('submitted', 'approved')
  ),
  claimed as (
    select coalesce(sum(amount), 0)::integer as total
    from public.payouts
    where tester_id = tester
      and status in ('pending', 'processing', 'paid')
  )
  select greatest(0, earned.total - claimed.total)::integer
  from earned, claimed
$$;

create or replace function public.set_submission_payout()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  experiment_budget integer;
begin
  select budget into experiment_budget
  from public.experiments
  where id = new.experiment_id;

  if experiment_budget is null then
    raise exception 'Experiment not found';
  end if;

  new.payout := round(experiment_budget * 0.3)::integer;
  return new;
end;
$$;

drop trigger if exists submissions_set_payout on public.submissions;
create trigger submissions_set_payout
before insert on public.submissions
for each row execute function public.set_submission_payout();

